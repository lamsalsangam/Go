package main

import "fmt"

func main() {
	fmt.Println("Go has various value types including strings, integers, floats, booleans," + " etc.")
	fmt.Println("")
	fmt.Println("1+1 =", 1+1)
	fmt.Println("10.0/2.0 =", 10.0/2.0)
	fmt.Println("")
	fmt.Println("true && false =", true && false)
	fmt.Println("true || false =", true || false)
	fmt.Println("!true =", !true)
}
